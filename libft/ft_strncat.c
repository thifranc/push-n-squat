#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	i2;

	i = ft_strlen(s1);
	i2 = 0;
	while (i2 < n)
	{
		s1[i + i2] = s2[i2];
		i2++;
	}
	s1[i + i2] = '\0';
	return (s1);
}
