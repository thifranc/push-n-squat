#include "libft.h"

int		ft_min(int *tab, int size)
{
	int	min;
	int	i;

	i = 1;
	min = tab[0];
	while (i < size)
	{
		if (min > tab[i])
			min = tab[i];
		i++;
	}
	return (min);
}
